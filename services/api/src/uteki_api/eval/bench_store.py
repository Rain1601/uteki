"""015 PR α · Eval workbench CRUD store.

Thin SQLModel CRUD over ``BenchmarkSuite`` and ``BenchmarkRun``. The
API layer (``api/admin.py``) wraps these calls with auth + validation;
this module is database mechanics only.

Auth contract: every caller of this store is already past
``Depends(require_admin)``. We do not re-check inside the store.
"""

from __future__ import annotations

import time
from typing import Any

from sqlmodel import Session, select

from uteki_api.eval.bench_models import BenchmarkRun, BenchmarkSuite


class BenchmarkSuiteStore:
    """Suite CRUD. ``id`` is auto-generated by the model's default_factory."""

    def create(
        self,
        db: Session,
        *,
        name: str,
        skill_name: str,
        queries: list[dict[str, Any]],
        created_by: str,
        description: str = "",
        cron_schedule: str | None = None,
    ) -> BenchmarkSuite:
        row = BenchmarkSuite(
            name=name,
            description=description,
            skill_name=skill_name,
            queries=queries,
            cron_schedule=cron_schedule,
            created_by=created_by,
        )
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def get(self, db: Session, suite_id: str) -> BenchmarkSuite | None:
        return db.get(BenchmarkSuite, suite_id)

    def get_by_name(
        self, db: Session, name: str, *, include_archived: bool = False
    ) -> BenchmarkSuite | None:
        stmt = select(BenchmarkSuite).where(BenchmarkSuite.name == name)
        if not include_archived:
            stmt = stmt.where(BenchmarkSuite.archived.is_(False))  # type: ignore[attr-defined]
        return db.exec(stmt).first()

    def list(
        self,
        db: Session,
        *,
        skill_name: str | None = None,
        include_archived: bool = False,
        limit: int = 200,
    ) -> list[BenchmarkSuite]:
        stmt = select(BenchmarkSuite)
        if skill_name is not None:
            stmt = stmt.where(BenchmarkSuite.skill_name == skill_name)
        if not include_archived:
            stmt = stmt.where(BenchmarkSuite.archived.is_(False))  # type: ignore[attr-defined]
        stmt = stmt.order_by(BenchmarkSuite.created_at.desc()).limit(limit)  # type: ignore[attr-defined]
        return list(db.exec(stmt).all())

    def update(
        self,
        db: Session,
        suite_id: str,
        *,
        name: str | None = None,
        description: str | None = None,
        queries: list[dict[str, Any]] | None = None,
        cron_schedule: str | None | object = ...,
    ) -> BenchmarkSuite | None:
        """Partial update — only fields you pass change.

        ``cron_schedule`` uses sentinel ``...`` to distinguish "don't touch"
        from "set to None" (disable cron). Both are valid intents.
        """
        row = self.get(db, suite_id)
        if row is None:
            return None
        if name is not None:
            row.name = name
        if description is not None:
            row.description = description
        if queries is not None:
            row.queries = queries
        if cron_schedule is not ...:
            row.cron_schedule = cron_schedule  # type: ignore[assignment]
        row.updated_at = time.time()
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def archive(self, db: Session, suite_id: str) -> bool:
        row = self.get(db, suite_id)
        if row is None or row.archived:
            return False
        row.archived = True
        row.updated_at = time.time()
        db.add(row)
        db.commit()
        return True


class BenchmarkRunStore:
    """Bench run CRUD. Lifecycle: queued → running → done | error | cancelled."""

    def create(
        self,
        db: Session,
        *,
        suite_id: str,
        mode: str,
        skill_name: str,
        skill_version_a: str,
        skill_version_b: str | None,
        n_per_query: int,
        temperature: float,
        triggered_by: str,
        triggered_by_user_id: str,
    ) -> BenchmarkRun:
        row = BenchmarkRun(
            suite_id=suite_id,
            mode=mode,
            skill_name=skill_name,
            skill_version_a=skill_version_a,
            skill_version_b=skill_version_b,
            n_per_query=n_per_query,
            temperature=temperature,
            triggered_by=triggered_by,
            triggered_by_user_id=triggered_by_user_id,
        )
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def get(self, db: Session, bench_run_id: str) -> BenchmarkRun | None:
        return db.get(BenchmarkRun, bench_run_id)

    def list_by_suite(
        self, db: Session, suite_id: str, *, limit: int = 50
    ) -> list[BenchmarkRun]:
        stmt = (
            select(BenchmarkRun)
            .where(BenchmarkRun.suite_id == suite_id)
            .order_by(BenchmarkRun.triggered_at.desc())  # type: ignore[attr-defined]
            .limit(limit)
        )
        return list(db.exec(stmt).all())

    def list_recent(
        self, db: Session, *, skill_name: str | None = None, limit: int = 50
    ) -> list[BenchmarkRun]:
        stmt = select(BenchmarkRun)
        if skill_name is not None:
            stmt = stmt.where(BenchmarkRun.skill_name == skill_name)
        stmt = stmt.order_by(BenchmarkRun.triggered_at.desc()).limit(limit)  # type: ignore[attr-defined]
        return list(db.exec(stmt).all())

    def mark_running(self, db: Session, bench_run_id: str) -> BenchmarkRun | None:
        row = self.get(db, bench_run_id)
        if row is None:
            return None
        row.status = "running"
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def mark_done(
        self,
        db: Session,
        bench_run_id: str,
        *,
        run_ids: list[str],
        metrics_summary: dict[str, Any],
    ) -> BenchmarkRun | None:
        row = self.get(db, bench_run_id)
        if row is None:
            return None
        row.status = "done"
        row.finished_at = time.time()
        row.run_ids = run_ids
        row.metrics_summary = metrics_summary
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def mark_error(
        self, db: Session, bench_run_id: str, *, error_message: str
    ) -> BenchmarkRun | None:
        row = self.get(db, bench_run_id)
        if row is None:
            return None
        row.status = "error"
        row.finished_at = time.time()
        row.error_message = error_message
        db.add(row)
        db.commit()
        db.refresh(row)
        return row

    def record_decision(
        self,
        db: Session,
        bench_run_id: str,
        *,
        decision: str,  # "approve" | "reject"
        user_id: str,
        reason: str = "",
    ) -> BenchmarkRun | None:
        """Anti-pattern guard: decision is *registered* here but does NOT
        deploy / change prompt files. The eval workbench surfaces signal;
        the human + git is the deployer."""
        row = self.get(db, bench_run_id)
        if row is None:
            return None
        now = time.time()
        if decision == "approve":
            row.approved_by = user_id
            row.approved_at = now
        elif decision == "reject":
            row.rejected_by = user_id
            row.rejected_at = now
            row.rejected_reason = reason
        else:
            raise ValueError(f"unknown decision {decision!r}; expected approve|reject")
        db.add(row)
        db.commit()
        db.refresh(row)
        return row


default_suite_store = BenchmarkSuiteStore()
default_bench_run_store = BenchmarkRunStore()
